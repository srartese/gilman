				 <h3> Chapter 2: London </h3>
				 <h4> August 26, 2016 </h4>
				 <p class="postcontent">
					 Our arrival to London was less than exciting. Public transportation seems to be a constant struggle and unfortunately taxis are overly expensive (we learned that the hard way).  London had so many tourists all around the major attractions so it was nice to wander in the less populated areas.  The best place to nap during the mid-afternoon is in St.James Park.  We spent a few afternoons here and it was so relaxing to read under the large trees.  One of my favorite places.
						<br />
					 We went saw Buckingham of course but we also went through Kensington Palace where they had a beautiful garden to walk around and there were not very many tourist because it is a bit of a further walk from the center attractions in London.  I would highly recommend taking a walk through Hyde Park which is way bigger than the map can even show.  It is huge and there are some free art exhibits you will pass inside.  Feels like you're walking through a field of flower gardens and somehow end up along a beach.  It is amazing. Another amazing thing was this cuban place by our hostel.  It was called Cubana and all I have to say is to go.  Go as fast as you can but don't be deterred if there is a pack of people outside, they are probably just there to drink.  There are seats inside.  				
					 <br />
					 Other fun attractions were the London Eye, Tower Of London, and the British Museum.  The London Eye is a good thing to do if you have never been before but the earlier you go, the better chance you have of not waiting all day.  The Tower of London was great to walk along the water to get to and great view of the bridge.  The inside also had a lot of historic things to see like the crown jewels and the different types of amor throughout the years.  Finally, the British Museum was amazing and free!  It had more things to see than I even thought possible. Would recommend to give enough time to go through the exhibits you want to see but don't try to see everything.  It would be an overload of history. 
					 <div class="listpnt">
					 <br />Take away: 
				<ul>
					<li> Don't take a taxi. Just don't.</li>
					<li> Ask your hostel for discount codes on tourist attractions</li>
					<li> Happy hour is VERY popular (and busy) so if you're hungry good luck but it will be worth it.</li>
				</ul>
					 </div>
				 </p>
