				 <h3> Chapter 1: Ireland </h3>
				 <h4> August 21, 2016 </h4>
				 <p class="postcontent"> 
					 Dublin is a beautiful place! It might just be the accents but everyone seems very nice and welcoming.  Our arrival began with not understanding public transportation and having a man seeing our confusion, offering some helpful advice.  We proceeded to ride in a taxi with a very friendly Irish man who told us about his family and his many visits to the United States (as much as I could understand).  Our Air BnB was kind enough to let us wake them up at 7am to leave our belongings until we could check in around 2pm so the rest of our day was spent adventuring and trying not to fall asleep in public. Day one we spent seeing Trinity College and the book of Kells, parks, Temple Bar, and the Jameson Distillery.  Day two was a day full of Guinness Storehouse which was well worth it.  They have an amazing view from the sky bar of the rolling hills and the city. In a few days I learned so much of the history and process that goes into making a proud whiskey and beer.  
				<br />
				Aside from the liquids consumed we did find some great places to eat. This first was a random place we found walking and I enjoyed the most delicious burger ever found.  The second was the Brazen Head, which was a very cute styled pub with a great beef stew. Overall what I saw of Dublin was great. A little rain and a lot of walking was a good change of pace compared to Rochester. 
				
