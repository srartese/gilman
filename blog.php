				 	I spent four months living and breathing Croatia life.  Overall it was a change but change makes everything more fun and broadens your perspective.  I would have never gotten the chance to see all the countries I did.  Flying home I had time to really think about everything I had experienced.  The people I met have taught me what it is like to live somewhere else and a new way to do it.  I never had the chance to really experience living anywhere other than America.  It still is very hard for me to explain exactly what it was like but I feel more open to new things. I was always very stuck in what I knew and with food especially I was afraid to try new things.  People everywhere are all the same but we just find our own way to go day by day.  I will go back to Croatia one day and I will continue to tell everyone about my time there.  I am so happy I went and some days I am sad that I have to be back here.  If there is one thing I will not miss is the days of traveling it takes to get to and from, everything else is beyond anything I could have ever expected.
